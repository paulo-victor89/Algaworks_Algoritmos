package Capitulo03;

public class TiposNumericos {
	public static void main(String[] args) {

		Byte numeroByte = 1; // vai de -128 até 127

		Short numeroShort = 1; // vai de -32768 até 32767

		Integer numeroInteger = 1; // vai de -21477883648 até 21477883647

		Long numeroLong = 1L; // vai de -9,223372037x10^18 até 9,223372037x10^18

		Float numeroFloat = 1.0f;

		Double numeroDouble = 1.0;
	}

}
