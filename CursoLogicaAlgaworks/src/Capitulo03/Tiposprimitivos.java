package Capitulo03;

public class Tiposprimitivos {

	public static void main(String[] args) {

		/* - Tipos primitivos não aceitam o valor null.
		 * - Basta colocar com a primeira letra minuscula e o mesmo fica primitivo.
		 * - No desenvolvimento do curso sera utilizado os tipos normais.
		 * - String não tem um tipo primitivo, sempre deve ser iniciado com S maiusculo
		 * 
		 */
		Byte variavelByte = 1;

		Short variavelShort = 1;

		// Será utilizado para inteiros
		Integer variavelInt = 1;

		Long variavelLong = 1L;

		Float variavelFloat = 1.0f;

		//Utilizado em decimais
		Double variavelDouble = 1.0;

		//Guarda somente um caracther ou digito
		Character variavelCharacter = 'A';

		Boolean variavelBoolean = true;
	}
}
